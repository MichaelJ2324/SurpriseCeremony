        <h2>The Story</h2>
    </header>
    <div class="container">
        <p>
            Alex and I have been together for 8 years, and as of August when the event is taking place, we will be married for five of those. I started writing down our story here, but realized it would take up quite a bit of space, as 8 years is quite a long time. Instead
            of that story, I'm opting to write a bit about why I'm attempting this event to begin with...
        </p>
        <p>
            Four years and some odd months ago, Alex and I knew we were going to get married, we just didn't know when. My sister Kristen was getting married at the time, and after going to her wedding and looking ahead at our lives, we realized that for us we couldn't wait any longer.
            We knew we loved each other, and we knew that for us to accomplish the things we wanted for ourselves, we had to make big decisions at that time, to make it <i>easier</i> on our future. Alex's University was expensive, and due to their policies, she had to live on Campus, since
            she didn't have family in the area. Getting married avoided that financial burden, as she could move into my apartment and we knew that would help us in the long run. So we made the big decision, and jumped right into it. On Wednesday August 1, 2012 we went down to the Fort Wayne
            Court House with her mother, Sheila, sister, Kelsey, and two friends at the time and vowed we would spend the rest of our lives together. It was a great day, and we said at the time, that in a year or so we would do a full wedding and celebrate with the rest of the family.
        </p>
        <p>
            Both of our immediate families knew we were married, and although we asked they keep in quiet, since we wanted to do a full wedding at a later time, it didn't stay quiet for long. As time went on, and everyone we knew, knew that we were married it seemed their wasn't a need for a big
            wedding ceremony. We were part of several wedding's in that time, and seeing the money spent and time it takes, made us say it wasn't worth it. At one point we started looking at venues, and we thought the Indianapolis Public Library looked like a great place to have a wedding, but decided
            it just wasn't where we wanted to put ourselves financially as we were finishing college. Life went on...I graduated and was working full time, Alex then graduated and started working, and it wasn't something we wanted to put effort toward. We got pets, we moved a bunch, bought a house, and
            overall were happy with the life we had before us, and were happy with not having had a full wedding ceremony.
        </p>
        <p>
            However, privately I always remembered the Public Library we had looked at, and remember every time she mentions how she does slightly wish we would've had a wedding. As we've grown through the years, we started to feel like we missed out on that special day. I know she wishes she would've
            gotten to buy that fancy dress, but there was something bigger that we felt we missed. A wedding isn't just about the couple getting married, it is also about the families that are connected to those people. There's a joining of two world's on a wedding day, and we started to feel like we
            missed that part in our journey. So since Alex doesn't want to deal with the hassle of a wedding, but still wanted that day to unite our families, I have had it in the back of my mind that I would do this as a surprise for one of our anniversaries. Lucky for me, Alex got a job promotion
            and we moved to the Indianapolis area. Once we moved down here, I started looking into our near future, and saw that I could finally make it happen, so I began planning.
        </p>
        <p>
            So now I've got this whole thing started. I've got the venue booked, guests invited, and a website to track it all. The event will be at a library, so I want it to center around stories. Alex loves to read and I like to create, so please join us in completing a small chapter of our lives
            with the celebration in August, and check back for more stories and details to come.
        </p>
    </div>


